import { ref, onMounted, computed } from 'vue'
import { ElMessage, ElMessageBox } from 'element-plus'
import { useRoute, useRouter } from 'vue-router'
import { ArrowLeft } from '@element-plus/icons-vue'
import { useI18n } from 'vue-i18n'
import { pageLayoutApi, businessObjectApi, type PageLayout, type AvailableField } from '@/api/system'
import { legacyTypeToMode } from '@/constants/layoutTemplates'
import type { LayoutMode } from '@/types/layout'
import { useLayoutFields } from '@/composables'
import { normalizeLayoutType } from '@/utils/layoutMode'
import { preparePersistLayoutConfig, resolveRawLayoutConfig } from '@/platform/layout/layoutPersistGuard'

const { t } = useI18n()
const route = useRoute()
const router = useRouter()

const objectCode = computed(() => route.params.objectCode as string || route.query.objectCode as string || '')
const objectName = ref(route.query.objectName as string || t('system.businessObject.title'))
const businessObjectId = ref(route.query.businessObjectId as string || '')
const loading = ref(false)
const tableData = ref<PageLayout[]>([])
const createVisible = ref(false)
const submitting = ref(false)

const createForm = ref({
  layoutCode: '',
  layoutName: '',
  description: '',
  isDefault: false
})

const DESIGNER_MODE: LayoutMode = 'edit'
const DESIGNER_LAYOUT_TYPE = 'edit'
const AUDIT_FIELD_CODES = new Set(['created_at', 'created_by', 'updated_at', 'updated_by', 'createdAt', 'createdBy', 'updatedAt', 'updatedBy'])

const isAuditFieldCode = (code: string) => AUDIT_FIELD_CODES.has(String(code || '').trim())

// Normalize layout mode from legacy layoutType
const getLayoutMode = (layout: PageLayout): LayoutMode => {
  // If mode is already set, use it
  if (layout.mode) return layout.mode as LayoutMode
  // Otherwise map from legacy layoutType
  if (layout.layoutType) return legacyTypeToMode(layout.layoutType)
  return 'edit'
}

// Layout counts by mode
const getManagementMode = (layout: PageLayout): LayoutMode => {
  // Unified management view keeps readonly layouts out of edit tab.
  // Readonly rendering is handled by mode overrides on shared edit layout.
  return getLayoutMode(layout)
}

const isUnifiedManagementLayout = (layout: PageLayout): boolean => {
  const rawType = String((layout as any).layoutType || (layout as any).layout_type || '').toLowerCase()
  // Single-layout management only configures the shared form/edit model.
  if (rawType && rawType !== 'form') return false

  if (getManagementMode(layout) !== 'edit') return false

  // Hide stale demoted generated rows (non-default *_default_form records).
  const code = String((layout as any).layoutCode || (layout as any).layout_code || '')
  if (!layout.isDefault && /_default_form$/i.test(code)) return false

  return true
}

// Single layout management: only edit/form rows are configurable.
const filteredLayouts = computed(() => {
  return tableData.value.filter(isUnifiedManagementLayout)
})

const getLayoutModeLabel = (mode: LayoutMode) => {
  if (mode === 'edit') return t('system.pageLayout.modes.edit')
  if (mode === 'readonly') return t('system.pageLayout.modes.readonly')
  return mode
}

const getStatusLabel = (status: string) => {
  const map: Record<string, string> = {
    'draft': t('system.pageLayout.status.draft'),
    'published': t('system.pageLayout.status.published'),
    'archived': t('system.pageLayout.status.archived')
  }
  return map[status] || status
}

const getStatusTag = (status: string) => {
  const tags: Record<string, any> = {
    'draft': 'info',
    'published': 'success',
    'archived': 'warning'
  }
  return tags[status] || 'info'
}

const getEmptyDescription = () => {
  const modeLabel = getLayoutModeLabel('edit')
  return t('system.pageLayout.messages.emptyState', { mode: modeLabel })
}

const ensureBusinessObjectId = async (): Promise<string> => {
  if (businessObjectId.value) return businessObjectId.value
  if (!objectCode.value) return ''
  const bo: any = await businessObjectApi.detail(objectCode.value)
  const resolvedId = bo?.id || bo?.data?.id || ''
  if (resolvedId) {
    businessObjectId.value = resolvedId
    if (!objectName.value && (bo?.name || bo?.data?.name)) {
      objectName.value = bo?.name || bo?.data?.name
    }
  }
  return businessObjectId.value
}

// Use useLayoutFields composable for field metadata with unified API layer
const {
  availableFields,
  fetchFields: fetchLayoutFields,
  loading: fieldsLoading,
  clearCache: clearFieldsCache
} = useLayoutFields(objectCode.value)

// Internal field definitions for backward compatibility
const fieldDefinitions = ref<any[]>([])

/**
 * Load field definitions using useLayoutFields composable
 * Refactored to use unified API layer instead of direct API call
 */
const loadFieldDefinitions = async () => {
  if (!objectCode.value) return

  try {
    // Use the useLayoutFields composable which uses businessObjectApi internally
    await fetchLayoutFields({ includeRelations: false })

    // Transform AvailableField[] to internal format for backward compatibility
    fieldDefinitions.value = availableFields.value.map((field: AvailableField) => ({
      id: field.id || field.code,
      code: field.code,
      name: field.name || field.label,
      fieldType: field.fieldType,
      isRequired: field.isRequired,
      isReadonly: field.isReadonly || field.readonly,
      isSystem: !field.isEditable || field.code === 'id',
      isSearchable: field.showInDetail,
      sortOrder: field.sortOrder || 0,
      description: field.description || field.helpText || '',
      showInList: field.showInList,
      showInForm: field.showInForm,
      showInDetail: field.showInDetail,
      referenceModelPath: field.relatedObject
    }))
  } catch (error) {
    console.error('Failed to load field definitions:', error)
    fieldDefinitions.value = []
  }
}

const loadLayouts = async () => {
  loading.value = true
  try {
    // The request interceptor unwraps { success: true, data: ... } automatically
    // So res is already the data array
    const layouts = await pageLayoutApi.byObject(objectCode.value)
    tableData.value = layouts || []

    // If no custom layouts exist, add system default layouts
    if (tableData.value.length === 0) {
      await addSystemDefaultLayouts()
    }
  } catch (error) {
    console.error('Failed to load page layouts:', error)
    // Start with empty array - user can create new layouts
    tableData.value = []
    await addSystemDefaultLayouts()
  } finally {
    loading.value = false
  }
}

// Add system-generated default layouts based on field definitions
const addSystemDefaultLayouts = async () => {
  const modes: Array<LayoutMode> = ['edit']

  for (const mode of modes) {
    // Check if layout of this mode exists
    const exists = tableData.value.some(l => getLayoutMode(l) === mode)
    if (exists) continue

    let layoutConfig = generateDefaultLayoutConfig(mode)

    try {
      // Prefer backend-generated default layout if available
      const defaultLayout: any = await pageLayoutApi.getDefault(objectCode.value, mode)
      const defaultData = (defaultLayout as any)?.data ?? defaultLayout
      const backendConfig = defaultData?.layoutConfig || defaultData?.layout_config
      if (backendConfig) {
        layoutConfig = backendConfig
      }
    } catch (error) {
      // Fall back to client-generated default layout
    }

    // Add system default layout
    tableData.value.push({
      id: `system_${mode}`,
      layoutCode: `system_default_${mode}`,
      layoutName: `${objectName.value || objectCode.value} ${t('system.pageLayout.types.form')} (${t('system.pageLayout.tags.systemDefault')})`,
      mode: mode,
      description: t('system.pageLayout.tips.default'),
      layoutConfig: layoutConfig,
      status: 'published',
      version: '1.0.0',
      isDefault: true,
      isActive: true,
      isSystem: true // Mark as system-generated
    } as any)
  }
}

// Generate default layout config from field definitions
// Backend returns camelCase directly via djangorestframework-camel-case
const generateDefaultLayoutConfig = (mode: LayoutMode) => {
  const fields = fieldDefinitions.value.filter(f => f.showInForm !== false && f.code !== 'id' && !isAuditFieldCode(f.code))

  // For edit and readonly modes - use sections structure
  const sections = []
  const basicFields = fields.filter(f =>
    ['code', 'name', 'status'].includes(f.code) ||
    (f.fieldType !== 'sub_table' && f.fieldType !== 'textarea' && f.fieldType !== 'rich_text')
  ).slice(0, 12)

  if (basicFields.length > 0) {
    sections.push({
      id: 'basic',
      type: 'section',
      title: t('system.businessObject.tabs.basic'),
      columnCount: 2,
      collapsible: false,
      fields: basicFields.map(f => ({
        fieldCode: f.code,
        label: f.name,
        span: f.fieldType === 'textarea' || f.fieldType === 'rich_text' ? 2 : 1,
        readonly: mode === 'readonly',
        required: f.isRequired,
        visible: true
      }))
    })
  }

  const textareaFields = fields.filter(f =>
    ['textarea', 'rich_text'].includes(f.fieldType)
  )
  if (textareaFields.length > 0) {
    sections.push({
      id: 'description',
      type: 'section',
      title: t('system.pageLayout.columns.description'),
      columnCount: 1,
      collapsible: true,
      fields: textareaFields.map(f => ({
        fieldCode: f.code,
        label: f.name,
        span: 1,
        readonly: mode === 'readonly',
        required: f.isRequired,
        visible: true
      }))
    })
  }

  return { sections }
}

const loadData = async () => {
  try {
    await ensureBusinessObjectId()
  } catch (error) {
    console.warn('Failed to resolve business object id:', error)
  }
  // Load field definitions first, then load layouts
  await loadFieldDefinitions()
  await loadLayouts()
}

