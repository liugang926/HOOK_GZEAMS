function normalizeHistoryEntries(raw: any): Array<Record<string, any>> {
  const payload = (raw as any)?.data ?? raw
  if (Array.isArray(payload)) return payload
  if (Array.isArray(payload?.results)) return payload.results
  if (Array.isArray(payload?.items)) return payload.items
  if (Array.isArray(payload?.history)) return payload.history
  return []
}

function resolveEntryVersion(entry: Record<string, any>): string | null {
  const candidates = [entry.version, entry.layoutVersion, entry.versionNumber, entry.version_number]
  for (const value of candidates) {
    if (value !== undefined && value !== null && String(value).trim() !== '') {
      return String(value)
    }
  }
  return null
}

function parseVersionNumber(value: unknown): number | null {
  if (typeof value === 'number' && Number.isFinite(value)) return value
  const raw = String(value ?? '').trim()
  if (!raw) return null
  const direct = Number(raw)
  if (Number.isFinite(direct)) return direct
  const match = raw.match(/-?\d+(?:\.\d+)?/)
  if (!match) return null
  const parsed = Number(match[0])
  return Number.isFinite(parsed) ? parsed : null
}

function pickRollbackVersion(currentVersion: unknown, entries: Array<Record<string, any>>): string | null {
  const currentRaw = String(currentVersion ?? '').trim()
  const normalized = entries
    .map((entry) => {
      const raw = resolveEntryVersion(entry)
      return raw ? { raw, num: parseVersionNumber(raw) } : null
    })
    .filter((item): item is { raw: string; num: number | null } => item !== null)

  const candidates = normalized.filter((item) => item.raw !== currentRaw)
  if (candidates.length === 0) return null

  const currentNum = parseVersionNumber(currentVersion)
  if (currentNum !== null) {
    const previousNumeric = candidates
      .filter((item) => item.num !== null && item.num < currentNum)
      .sort((a, b) => (b.num as number) - (a.num as number))
    if (previousNumeric.length > 0) return previousNumeric[0].raw
  }

  // Fall back to API order (typically newest -> oldest) and pick the first different version.
  return candidates[0].raw
}

const handleRollback = async (row: PageLayout) => {
  try {
    await ElMessageBox.confirm(
      t('system.pageLayout.messages.confirmRollback', { name: row.layoutName }),
      t('system.pageLayout.actions.rollback'),
      { type: 'warning' }
    )

    const historyResponse = await pageLayoutApi.history(row.id)
    const historyEntries = normalizeHistoryEntries(historyResponse)
    const targetVersion = pickRollbackVersion((row as any).version, historyEntries)

    if (!targetVersion) {
      ElMessage.warning(t('system.pageLayout.messages.noRollbackTarget'))
      return
    }

    await pageLayoutApi.rollback(row.id, targetVersion)
    await normalizeRolledBackLayout(row.id)
    ElMessage.success(t('system.pageLayout.messages.rollbackSuccess'))
    await loadData()
  } catch (error: any) {
    if (error !== 'cancel' && !error?.isHandled) {
      ElMessage.error(
        error.response?.data?.message ||
        error.message ||
        t('system.pageLayout.messages.rollbackFailed')
      )
    }
  }
}

const normalizeRolledBackLayout = async (layoutId: string) => {
  try {
    const latestLayout = await pageLayoutApi.detail(layoutId)
    const currentConfig = resolveRawLayoutConfig(latestLayout)
    const normalizedConfig = preparePersistLayoutConfig(latestLayout, {
      layoutType: 'form',
      dropFieldCode: isAuditFieldCode
    })

    if (JSON.stringify(currentConfig) !== JSON.stringify(normalizedConfig)) {
      await pageLayoutApi.partialUpdate(layoutId, {
        layoutConfig: normalizedConfig
      } as any)
    }
  } catch (error) {
    // Rollback success should not fail due to post-normalization best-effort.
    console.warn('normalizeRolledBackLayout skipped:', error)
  }
}

const handleToggleActive = async (row: PageLayout) => {
  try {
    await pageLayoutApi.partialUpdate(row.id, { is_active: !row.isActive })
    row.isActive = !row.isActive
    ElMessage.success(row.isActive ? t('common.status.enabled') : t('common.status.inactive'))
  } catch (error) {
    ElMessage.error(t('common.messages.operationFailed'))
  }
}

const handleDelete = async (row: PageLayout) => {
  try {
    await pageLayoutApi.delete(row.id)
    ElMessage.success(t('common.messages.deleteSuccess'))
    await loadData()
  } catch (error) {
    ElMessage.error(t('common.messages.deleteFailed'))
  }
}


// Handle customize button for system default layouts
// Creates a new custom layout based on the system default configuration
const handleCustomize = async (row: PageLayout) => {
  try {
    const targetBusinessObjectId = await ensureBusinessObjectId()
    if (!targetBusinessObjectId) {
      throw new Error('businessObjectId is required')
    }
    const mode: LayoutMode = DESIGNER_MODE
    const customTimestamp = Date.now()
    const customLayoutCode = `${String(objectCode.value || 'object').toLowerCase()}_form_custom_${customTimestamp}`
    const customLayoutName = `${objectName.value || objectCode.value} ${t('system.pageLayout.types.form')} - ${t('system.pageLayout.actions.customize')}`
    const customLayoutConfig = preparePersistLayoutConfig(row, {
      layoutType: 'form',
      dropFieldCode: isAuditFieldCode
    })
    // Create a custom layout from the system default
    // The request interceptor unwraps the response, so customLayout is already the data
    const customLayout = await pageLayoutApi.create({
      layoutCode: customLayoutCode,
      layoutName: customLayoutName,
      mode,
      layoutType: normalizeLayoutType(mode),
      businessObject: row.business_object || row.businessObject || targetBusinessObjectId,
      description: `Custom layout based on ${row.layoutName || customLayoutName}`,
      status: 'draft',
      version: '0.1.0',
      isDefault: false,
      layoutConfig: customLayoutConfig
    })
    ElMessage.success(t('common.messages.createSuccess'))
    // Open designer with the new custom layout
    router.push({
      name: 'PageLayoutDesigner',
      query: {
        layoutId: customLayout.id,
        layoutName: customLayout.layoutName,
        layoutType: DESIGNER_LAYOUT_TYPE,
        objectCode: objectCode.value,
        objectName: objectName.value,
        businessObjectId: customLayout.business_object || customLayout.businessObject || targetBusinessObjectId
      }
    })
    await loadData()
  } catch (error: any) {
    ElMessage.error(error.response?.data?.message || t('common.messages.operationFailed'))
  }
}

