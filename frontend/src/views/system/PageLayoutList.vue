const handleCreateSubmit = async () => {
  if (!createForm.value.layoutCode || !createForm.value.layoutName) {
    ElMessage.warning(t('system.pageLayout.messages.required'))
    return
  }

  submitting.value = true
  try {
    const targetBusinessObjectId = await ensureBusinessObjectId()
    if (!targetBusinessObjectId) {
      throw new Error('businessObjectId is required')
    }
    const mode: LayoutMode = 'edit'
    const layoutType = normalizeLayoutType(mode)
    const layoutConfig = preparePersistLayoutConfig(getDefaultLayoutConfig(mode), {
      layoutType: 'form',
      dropFieldCode: isAuditFieldCode
    })
    // The request interceptor unwraps { success: true, data: ... } automatically
    const newLayout = await pageLayoutApi.create({
      layoutCode: createForm.value.layoutCode,
      layoutName: createForm.value.layoutName,
      mode,
      layoutType,
      description: createForm.value.description,
      isDefault: createForm.value.isDefault,
      businessObject: targetBusinessObjectId,
      status: 'draft',
      version: '0.1.0',
      layoutConfig
    })
    ElMessage.success(t('system.pageLayout.messages.createSuccess'))
    createVisible.value = false
    // Open designer with new layout (response is already unwrapped)
    router.push({
      name: 'PageLayoutDesigner',
      query: {
        layoutId: newLayout.id,
        layoutName: newLayout.layoutName,
        layoutType: DESIGNER_LAYOUT_TYPE,
        objectCode: objectCode.value,
        objectName: objectName.value,
        businessObjectId: newLayout.businessObject || newLayout.business_object || targetBusinessObjectId
